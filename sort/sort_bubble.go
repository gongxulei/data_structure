// BubblingSort 冒泡排序：每次循环将数字跟下一位比较指导排序完成
// 时间复杂度 n*n + n*(n-1) + n*(n-2) + ... + n*2 + n*1 + n*0 = n²/2 = O(n²)
// 空间复杂度 1
func BubblingSort(arr []int) []int {
	for i := len(arr) - 1; i > 0; i-- {
		mv := 0
		for j := 0; j < i; j++ {
			if arr[j] > arr[j+1] {
				temp := arr[j+1]
				arr[j+1] = arr[j]
				arr[j] = temp
				mv++
			}
		}
		if mv == 0 {
			fmt.Println("i: ", i)
			return arr
		}
	}
	return arr
}
