package main

// InsertSorted 插入排序
// 时间复杂度 O(n²) 空间复杂度O(1)
func InsertSorted(arr []int) []int {
	for i := 1; i < len(arr)-1; i++ {
		for j := i; j > 0; j-- {
			// 交换
			if arr[j] < arr[j-1] {
				temp := arr[j]
				arr[j] = arr[j-1]
				arr[j-1] = temp
			}
		}
	}
	return arr
}
